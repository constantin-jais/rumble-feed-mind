-- Enforce tenant isolation after externally provisioning the roles documented in
-- scripts/postgres/provision-roles.sql. This migration is intentionally atomic:
-- runtime role grants, policies, and FORCE ROW LEVEL SECURITY land together.

REVOKE CREATE ON SCHEMA public FROM PUBLIC;
GRANT USAGE, CREATE ON SCHEMA public TO feed_radar_owner;
GRANT USAGE ON SCHEMA public TO feed_radar_app, feed_radar_auth, feed_radar_worker, feed_radar_readonly;

CREATE OR REPLACE FUNCTION feed_radar_tenant_id()
RETURNS UUID
LANGUAGE SQL
STABLE
SECURITY INVOKER
SET search_path = pg_catalog
AS $$
    SELECT NULLIF(current_setting('app.user_id', true), '')::UUID
$$;

REVOKE ALL ON FUNCTION feed_radar_tenant_id() FROM PUBLIC;
GRANT EXECUTE ON FUNCTION feed_radar_tenant_id() TO feed_radar_app, feed_radar_readonly;

-- Authentication is the only unauthenticated path to users. These functions
-- execute with the migration owner, pin search_path, and expose only reviewed
-- operations; feed_radar_auth receives no direct table privilege.
CREATE OR REPLACE FUNCTION feed_radar_auth_find_user(requested_email TEXT)
RETURNS TABLE (
    id UUID,
    email VARCHAR(255),
    password_hash VARCHAR(255),
    display_name VARCHAR(100),
    tier VARCHAR(20)
)
LANGUAGE SQL
STABLE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    SELECT users.id, users.email, users.password_hash, users.display_name, users.tier
    FROM public.users
    WHERE users.email = requested_email AND users.deleted_at IS NULL
$$;

CREATE OR REPLACE FUNCTION feed_radar_auth_register(
    requested_email TEXT,
    requested_password_hash TEXT,
    requested_display_name TEXT
)
RETURNS TABLE (
    id UUID,
    email VARCHAR(255),
    password_hash VARCHAR(255),
    display_name VARCHAR(100),
    tier VARCHAR(20)
)
LANGUAGE SQL
VOLATILE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    INSERT INTO public.users (email, password_hash, display_name, tier)
    VALUES (requested_email, requested_password_hash, requested_display_name, 'free')
    RETURNING users.id, users.email, users.password_hash, users.display_name, users.tier
$$;

CREATE OR REPLACE FUNCTION feed_radar_auth_touch_last_login(requested_user_id UUID)
RETURNS VOID
LANGUAGE SQL
VOLATILE
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
    UPDATE public.users SET last_login_at = NOW() WHERE id = requested_user_id
$$;

REVOKE ALL ON FUNCTION feed_radar_auth_find_user(TEXT) FROM PUBLIC;
REVOKE ALL ON FUNCTION feed_radar_auth_register(TEXT, TEXT, TEXT) FROM PUBLIC;
REVOKE ALL ON FUNCTION feed_radar_auth_touch_last_login(UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION feed_radar_auth_find_user(TEXT) TO feed_radar_auth;
GRANT EXECUTE ON FUNCTION feed_radar_auth_register(TEXT, TEXT, TEXT) TO feed_radar_auth;
GRANT EXECUTE ON FUNCTION feed_radar_auth_touch_last_login(UUID) TO feed_radar_auth;

