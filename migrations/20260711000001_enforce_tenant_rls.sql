-- Worker access is explicit per operation group. It is cross-tenant by design,
-- but never relies on ownership, superuser, or BYPASSRLS.
GRANT SELECT, UPDATE, DELETE ON users TO feed_radar_worker;
GRANT SELECT, UPDATE ON feeds TO feed_radar_worker;
GRANT SELECT, INSERT, UPDATE, DELETE ON articles TO feed_radar_worker;
GRANT SELECT, UPDATE ON rules TO feed_radar_worker;
GRANT INSERT ON rule_evaluations TO feed_radar_worker;
GRANT SELECT, INSERT, UPDATE, DELETE ON feed_categories TO feed_radar_worker;
GRANT SELECT, INSERT, UPDATE, DELETE ON
    stripe_customers, subscriptions, usage_records, usage_daily, invoices,
    payment_methods, billing_events, dunning_history, webhook_events
TO feed_radar_worker;

-- All UUID primary keys are generated by functions today; this also keeps future
-- owner-created sequences inaccessible unless deliberately granted.
REVOKE ALL ON ALL SEQUENCES IN SCHEMA public FROM PUBLIC;

-- Cross-table references must carry the same tenant. Existing single-column
-- foreign keys keep their delete semantics; these composite constraints add the
-- tenant invariant and deliberately fail migration on historical corruption.
ALTER TABLE folders ADD CONSTRAINT folders_id_user_key UNIQUE (id, user_id);
ALTER TABLE feeds ADD CONSTRAINT feeds_id_user_key UNIQUE (id, user_id);
ALTER TABLE rules ADD CONSTRAINT rules_id_user_key UNIQUE (id, user_id);
ALTER TABLE stripe_customers ADD CONSTRAINT stripe_customers_id_user_key UNIQUE (id, user_id);
ALTER TABLE subscriptions ADD CONSTRAINT subscriptions_id_user_key UNIQUE (id, user_id);

ALTER TABLE folders ADD CONSTRAINT folders_parent_tenant_fk
    FOREIGN KEY (parent_id, user_id) REFERENCES folders (id, user_id);
ALTER TABLE feeds ADD CONSTRAINT feeds_folder_tenant_fk
    FOREIGN KEY (folder_id, user_id) REFERENCES folders (id, user_id);
ALTER TABLE articles ADD CONSTRAINT articles_feed_tenant_fk
    FOREIGN KEY (feed_id, user_id) REFERENCES feeds (id, user_id);
ALTER TABLE articles ADD CONSTRAINT articles_hidden_rule_tenant_fk
    FOREIGN KEY (hidden_by_rule_id, user_id) REFERENCES rules (id, user_id);
ALTER TABLE rules ADD CONSTRAINT rules_feed_tenant_fk
    FOREIGN KEY (feed_id, user_id) REFERENCES feeds (id, user_id);
ALTER TABLE rules ADD CONSTRAINT rules_folder_tenant_fk
    FOREIGN KEY (folder_id, user_id) REFERENCES folders (id, user_id);
ALTER TABLE subscriptions ADD CONSTRAINT subscriptions_customer_tenant_fk
    FOREIGN KEY (stripe_customer_id, user_id) REFERENCES stripe_customers (id, user_id);
ALTER TABLE usage_records ADD CONSTRAINT usage_records_subscription_tenant_fk
    FOREIGN KEY (subscription_id, user_id) REFERENCES subscriptions (id, user_id);
ALTER TABLE invoices ADD CONSTRAINT invoices_subscription_tenant_fk
    FOREIGN KEY (subscription_id, user_id) REFERENCES subscriptions (id, user_id);
