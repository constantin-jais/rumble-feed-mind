-- Derived ownership must follow the declared foreign-key path. WITH CHECK also
-- validates the companion rule/tag reference to reject cross-tenant links.
CREATE POLICY rule_evaluations_owner ON rule_evaluations TO feed_radar_owner USING (true) WITH CHECK (true);
CREATE POLICY rule_evaluations_tenant ON rule_evaluations TO feed_radar_app, feed_radar_readonly
    USING (EXISTS (
        SELECT 1 FROM articles
        WHERE articles.id = rule_evaluations.article_id
          AND articles.user_id = feed_radar_tenant_id()
    ))
    WITH CHECK (
        EXISTS (
            SELECT 1 FROM articles
            WHERE articles.id = rule_evaluations.article_id
              AND articles.user_id = feed_radar_tenant_id()
        )
        AND EXISTS (
            SELECT 1 FROM rules
            WHERE rules.id = rule_evaluations.rule_id
              AND rules.user_id = feed_radar_tenant_id()
        )
    );
CREATE POLICY rule_evaluations_worker ON rule_evaluations TO feed_radar_worker USING (true) WITH CHECK (true);

CREATE POLICY article_tags_owner ON article_tags TO feed_radar_owner USING (true) WITH CHECK (true);
CREATE POLICY article_tags_tenant ON article_tags TO feed_radar_app, feed_radar_readonly
    USING (EXISTS (
        SELECT 1 FROM articles
        WHERE articles.id = article_tags.article_id
          AND articles.user_id = feed_radar_tenant_id()
    ))
    WITH CHECK (
        EXISTS (
            SELECT 1 FROM articles
            WHERE articles.id = article_tags.article_id
              AND articles.user_id = feed_radar_tenant_id()
        )
        AND EXISTS (
            SELECT 1 FROM tags
            WHERE tags.id = article_tags.tag_id
              AND tags.user_id = feed_radar_tenant_id()
        )
        AND (
            article_tags.rule_id IS NULL
            OR EXISTS (
                SELECT 1 FROM rules
                WHERE rules.id = article_tags.rule_id
                  AND rules.user_id = feed_radar_tenant_id()
            )
        )
    );

